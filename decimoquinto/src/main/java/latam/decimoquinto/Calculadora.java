package latam.decimoquinto;

/**
 *
 * @author cheshire
 */
public class Calculadora {
    
    public static int sumar(int uno, int dos) {
        return uno + dos;
    }
    
    public static int restar(int uno, int dos) {
        return uno - dos;
    }
    
    public static float dividir(int dividendo, int divisor) {
        return dividendo / divisor;
    }
    
    public static int multiplicar(int uno, int dos) {
        return uno * dos;
    }
    
}
